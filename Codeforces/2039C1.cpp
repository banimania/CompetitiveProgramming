#define PRINTLN(a) for (int i = 0; i < a.size(); ++i) { cout << a[i] << " "; } cout << endl
#define SORTASC(a) sort(a.begin(), a.end(), [](int x, int y) { return x < y; })
#define SORTDESC(a) sort(a.begin(), a.end(), [](int x, int y) { return x > y; })
#define FIND(a, target) find(a.begin(), a.end(), target) - a.begin()
#define UPPER(a, target) upper_bound(a.begin(), a.end(), target) - a.begin()
#define LOWER(a, target) lower_bound(a.begin(), a.end(), target) - a.begin()
#define MAX(a) distance(a.begin(), max_element(a.begin(), a.end()))
#define MIN(a) distance(a.begin(), min_element(a.begin(), a.end()))

#define TESTCASES true

void solve() {
  long long x, m;
  cin >> x >> m;

  set<int> xDiv;

  xDiv.insert(x);
  for (int i = 1; i <= sqrt(x); i++) {
    if (x % i == 0) xDiv.insert(i), xDiv.insert(x / i);
  }

  int ans = 0;
  for (int y = 1; y <= min(m, 4 * x); y++) {
    if (y == x) continue;

    int res = x ^ y;

    if (res != 0 && (xDiv.count(res) || (y % res) == 0)) {
      ans++;
    }
  }

  cout << ans << endl;
}

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  int t = 1;

  if (TESTCASES) cin >> t;
  while (t--) {
    solve();
  }
  return 0;
}
